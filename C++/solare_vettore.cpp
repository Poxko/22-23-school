//1. includo le librerie
#include <iostream>
using namespace std;

//2. inizio il blocco main
int main()
{

//3. dichiaro e inizializzo le variabili

	int tot=0;
	int somma=0;
	int i;
	int vettore[i];

//4. input
	
	cin>>tot;

//5. logica - operazioni - algoritmo
	
	for(i=0;tot>i;i=i+1)
	{
		cin>>vettore[i];
		somma=somma+vettore[i];
	}

//6. output

	cout<<somma;

//7. ritorno al sistema operativo


//8. fine del programma
}
